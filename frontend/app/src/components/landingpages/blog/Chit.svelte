<section>
    <h2>How to enter the airdrops?</h2>
    <p>
        To enter the airdrop you must join a community called <a
            href="https://oc.app/community/txydz-jyaaa-aaaaf-bifea-cai">CHIT for CHAT</a>
        which requires either <em>lifetime diamond membership</em> or (coming very soon) a
        <em>proof of unique personhood credential</em>
        from <ExternalLink href="https://decideai.xyz/">Decide AI</ExternalLink>. Within the
        community is a channel for the upcoming airdrop which requires standard diamond membership
        to join. The consequence of this double layer of gates is that to enter the airdrop you will
        need either lifetime diamond membership, or proof of unique personhood and standard diamond
        membership.
    </p>
    <p>
        The reason for these gates is to discourage mercenary users from farming tokens by creating
        multiple accounts. Some users are uncomfortable about proving they are a unique person
        because they feel it is an invasion of privacy. This is why there is the option to enter the
        airdrop by having lifetime diamond membership. It is unlikely a single person will try to
        farm CHAT by buying multiple lifetime diamond memberships because they are quite expensive.
        However, for future airdrops, if this does seem to be happening, we might remove this
        option.
    </p>
    <p>
        We are confident these measures will be sufficient but the development team also reserves
        the right to exclude users from this community if we determine they are deliberately trying
        to farm tokens.
    </p>
    <p>
        Note, the proof of unique personhood gate is not yet available but it will be in time for
        the first airdrop so it is not necessary to buy lifetime membership. If necessary we will
        postpone the airdrop until this gate is ready.
    </p>
</section>
<section>
    <h2>How much CHAT do I get for my CHIT?</h2>
    <p>
        This is a little complicated but we think offers a good balance of fairness and excitement.
    </p>
    <p>The 100,000 CHAT will be split into 2 pots of 80,000 CHAT and 20,000 CHAT.</p>
    <p>
        For every 5,000 CHIT you have earned you will receive 1 share of the first pot of 80,000
        CHAT.
    </p>
    <p>
        For example, if you have earned 36,500 CHIT then you will receive 7 shares. Say there are
        10,000 shares altogether you would receive (80,000 x 7 / 10,000) = 56 CHAT. Bear in mind
        that the total amount of CHAT in each airdrop is fixed so the more users that enter the
        lower the amount of CHAT per user.
    </p>
    <p>
        In addition, for every 25,000 CHIT you have earned you will receive 1 ticket in a lottery
        for the 20,000 CHAT pot. 3 tickets will be drawn randomly and the holders will receive
        12,000, 5,000 and 3,000 CHAT respectively.
    </p>
    <p>
        <b>EDIT:</b> For the end of August airdrop we are changing the lottery rules and prizes. 
        Airdrop participants with at least 25,000 CHIT or at least a 30 day streak will get a single
        lottery ticket. 10 tickets will be drawn randomly and the holders will receive 2000 CHAT each.
    </p>
</section>
