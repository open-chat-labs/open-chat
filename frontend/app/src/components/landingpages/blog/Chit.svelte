        consecutive days is called a streak and you earn more daily CHIT once you reach a 3 day
        streak. Your daily CHIT reward increases again at 7 days, 14 days and 30 days.
    </p>
    <img src="/assets/blog/chit/claim_streak.png" alt="Claim daily CHIT" />
    <p>
        You can also earn CHIT for various <em>achievements</em> such as sending your first message or
        uploading an avatar image. Also called "learn to earn", this encourages users to explore the
        features of OpenChat. At the time of writing there are 13 possible achievements but this will
        continue to increase over time until all aspects of OpenChat are covered.
    </p>
    <p>
        Some users have earned extra CHIT for winning a meme competition and we expect ad hoc CHIT
        rewards like this to continue.
    </p>
</section>
<section>
    <h2>Badges, leaderboard and calendar</h2>
